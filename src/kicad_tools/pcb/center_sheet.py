@dataclass
class CenterReport:
    """Summary of a ``center_pcb_text`` / ``center_on_sheet`` run."""

    paper_before: str
    paper_after: str
    bbox_before: tuple[float, float, float, float]
    bbox_after: tuple[float, float, float, float]
    dx_mm: float
    dy_mm: float
    translated_items: int
    usable_area: tuple[float, float, float, float]

    @property
    def paper_changed(self) -> bool:
        return self.paper_before != self.paper_after

    @property
    def changed(self) -> bool:
        return self.paper_changed or self.dx_mm != 0.0 or self.dy_mm != 0.0


def edge_cuts_bbox(text: str) -> tuple[float, float, float, float] | None:
    """Bounding box of all top-level Edge.Cuts graphics in raw PCB text.

    Arc extents are approximated by their start/mid/end points (same
    approximation as ``PCB._edge_cuts_bbox_sexp``).
    """
    xs: list[float] = []
    ys: list[float] = []
    root_open, root_close = _find_root(text)
    for tag, s, e in _iter_child_blocks(text, root_open, root_close):
        if tag not in ("gr_rect", "gr_line", "gr_arc", "gr_circle", "gr_poly", "gr_curve"):
            continue
        on_edge = False
        for ctag, cs, ce in _iter_child_blocks(text, s, e):
            if ctag == "layer":
                atoms = [text[a:b] for a, b in _iter_atoms(text, cs, ce)]
                on_edge = bool(atoms) and atoms[0].strip('"') == "Edge.Cuts"
                break
        if not on_edge:
            continue
        edits: list[tuple[int, int, str]] = []
        _collect_item_edits(text, s, e, 0, 0, edits)
        # Reuse the coordinate discovery: each coord node contributed an
        # (unchanged) X edit then Y edit, in atom order.
        for i in range(0, len(edits) - 1, 2):
            xs.append(float(edits[i][2]))
            ys.append(float(edits[i + 1][2]))
    if not xs:
        return None
    return (min(xs), min(ys), max(xs), max(ys))


def center_pcb_text(
    text: str,
    *,
    paper: str = "auto",
    margin: float = DEFAULT_FRAME_MARGIN_MM,
    title_block: float = DEFAULT_TITLE_BLOCK_MM,
    slack: float = DEFAULT_PAPER_SLACK_MM,
    grid: float = DEFAULT_GRID_MM,
) -> tuple[str, CenterReport]:
    """Center the board in its sheet's usable drawing area (pure text).

    Args:
        text: Raw ``.kicad_pcb`` file text.
        paper: ``"auto"`` (default) keeps a standard sheet when the board
            fits its usable area, escalates to the smallest standard
            landscape size otherwise, and always upgrades ``User`` sheets to
            a standard size.  ``"keep"`` never changes the sheet (errors if
            the board cannot fit a ``User`` sheet's usable area).  A specific
            size name (``"A4"``...``"A0"``) forces that sheet.
        margin: Frame border inset per side in mm.
        title_block: Reserved title-block band height in mm.
        slack: Required per-side slack for auto paper selection.
        grid: Grid to snap the translation delta to (mm).

    Returns:
        ``(new_text, CenterReport)``.  When nothing needs to change the
        original text object is returned unchanged (idempotent).
    """
    bbox = edge_cuts_bbox(text)
    if bbox is None:
        raise ValueError("center-on-sheet requires an Edge.Cuts board outline; none found.")
    min_x, min_y, max_x, max_y = bbox
    board_w = max_x - min_x
    board_h = max_y - min_y

    paper_node = _find_paper_node(text)
    if paper_node is None:
        raise ValueError("No (paper ...) node found in PCB.")
    p_start, p_end, cur_name, cur_w, cur_h = paper_node

    def fits(pw: float, ph: float) -> bool:
        x0, y0, x1, y1 = usable_area(pw, ph, margin=margin, title_block=title_block)
        return board_w <= (x1 - x0) and board_h <= (y1 - y0)

    new_name = cur_name
    if paper == "keep":
        if not fits(cur_w, cur_h):
            raise ValueError(
                f"Board ({board_w:g} x {board_h:g} mm) does not fit the usable "
                f"area of the current {cur_name} sheet ({cur_w:g} x {cur_h:g} mm) "
                f"with margin={margin:g} and title_block={title_block:g}."
            )
    elif paper == "auto":
        if cur_name == "User" or cur_name not in _PAPER_SIZES or not fits(cur_w, cur_h):
            selected = select_paper(
                board_w, board_h, slack=slack, margin=margin, title_block=title_block
            )
            if selected is None:
                raise ValueError(
                    f"Board ({board_w:g} x {board_h:g} mm) does not fit any "
                    f"standard sheet up to A0 with {slack:g} mm slack per side."
                )
            new_name = selected
    else:
        if paper not in _PAPER_SIZES:
            raise ValueError(
                f"Unknown paper {paper!r}. Use 'auto', 'keep' or one of: {', '.join(_PAPER_SIZES)}"
            )
        if not fits(*_PAPER_SIZES[paper]):
            raise ValueError(
                f"Board ({board_w:g} x {board_h:g} mm) does not fit the usable area of {paper}."
            )
        new_name = paper

    if new_name == cur_name:
        new_w, new_h = cur_w, cur_h
    else:
        new_w, new_h = _PAPER_SIZES[new_name]

    x0, y0, x1, y1 = usable_area(new_w, new_h, margin=margin, title_block=title_block)
    target_cx = (x0 + x1) / 2
    target_cy = (y0 + y1) / 2
    cur_cx = (min_x + max_x) / 2
    cur_cy = (min_y + max_y) / 2

    # Snap the delta ONCE to the grid, in exact integer nanometres; every
    # coordinate then shifts by this identical delta (true rigid translation).
    grid_nm = round(grid * _NM_PER_MM)
    dx_nm = round(round((target_cx - cur_cx) * _NM_PER_MM / grid_nm)) * grid_nm
    dy_nm = round(round((target_cy - cur_cy) * _NM_PER_MM / grid_nm)) * grid_nm

    report = CenterReport(
        paper_before=cur_name,
        paper_after=new_name,
        bbox_before=bbox,
        bbox_after=(
            min_x + dx_nm / _NM_PER_MM,
            min_y + dy_nm / _NM_PER_MM,
            max_x + dx_nm / _NM_PER_MM,
            max_y + dy_nm / _NM_PER_MM,
        ),
        dx_mm=dx_nm / _NM_PER_MM,
        dy_mm=dy_nm / _NM_PER_MM,
        translated_items=0,
        usable_area=(x0, y0, x1, y1),
    )

    if dx_nm == 0 and dy_nm == 0 and new_name == cur_name:
        return text, report  # already centered: no-op

    new_text = text
    if new_name != cur_name:
        new_text = new_text[:p_start] + f'(paper "{new_name}")' + new_text[p_end + 1 :]

    if dx_nm != 0 or dy_nm != 0:
        new_text, items = _translate_pcb_text(new_text, dx_nm, dy_nm)
        report.translated_items = items

    return new_text, report


def center_on_sheet(
    pcb_path: Path | str,
    *,
    output_path: Path | str | None = None,
    paper: str = "auto",
    margin: float = DEFAULT_FRAME_MARGIN_MM,
    title_block: float = DEFAULT_TITLE_BLOCK_MM,
    slack: float = DEFAULT_PAPER_SLACK_MM,
    grid: float = DEFAULT_GRID_MM,
    dry_run: bool = False,
) -> CenterReport:
    """Center a ``.kicad_pcb`` file on its drawing sheet.

    See :func:`center_pcb_text` for parameter semantics.  When *dry_run* is
    True nothing is written; otherwise the result is written to
    *output_path* (default: in place) only if something changed.
    """
    pcb_path = Path(pcb_path)
    text = pcb_path.read_text()
    new_text, report = center_pcb_text(
        text, paper=paper, margin=margin, title_block=title_block, slack=slack, grid=grid
    )
    if report.changed and not dry_run:
        dest = Path(output_path) if output_path is not None else pcb_path
        dest.write_text(new_text)
    return report
