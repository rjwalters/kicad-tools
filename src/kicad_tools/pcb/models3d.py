@dataclass
class ModelPatchReport:
    """Summary of an ``add_model_refs`` run."""

    patched: list[str] = field(default_factory=list)
    """Lib ids that had model refs inserted (one entry per footprint)."""
    already_present: list[str] = field(default_factory=list)
    """Lib ids skipped because the footprint already has a model ref."""
    unresolved: list[str] = field(default_factory=list)
    """Lib ids skipped: not found in the installed KiCad libraries."""
    no_model_in_library: list[str] = field(default_factory=list)
    """Lib ids skipped: library footprint exists but has no model ref."""
    variant_matches: dict[str, str] = field(default_factory=dict)
    """Lib ids whose model came from a same-library name variant
    (``lib_id -> matched footprint name``)."""
    substitution_matches: dict[str, str] = field(default_factory=dict)
    """Lib ids whose model came from a cross-library substitution
    (``lib_id -> substitute lib_id``)."""
    lcsc_matches: dict[str, str] = field(default_factory=dict)
    """Lib ids whose model came from the LCSC/EasyEDA fetch tier
    (``lib_id -> C-number``)."""

    @property
    def changed(self) -> bool:
        return bool(self.patched)


def add_model_refs_to_text(pcb_text: str, resolver: Resolver) -> tuple[str, ModelPatchReport]:
    """Insert missing ``(model ...)`` refs into raw ``.kicad_pcb`` text.

    Pure text insertion: the returned text differs from *pcb_text* only by
    inserted model lines (never removed or reordered bytes), keeping the
    change strictly render-metadata-scoped.
    """
    report = ModelPatchReport()
    insertions: list[tuple[int, str]] = []  # (index, text) sorted ascending

    for block in iter_footprint_blocks(pcb_text):
        body = pcb_text[block.start : block.end + 1]
        if _contains_token(body, "model"):
            report.already_present.append(block.lib_id)
            continue
        resolved = resolver(block.lib_id)
        if resolved is None:
            report.unresolved.append(block.lib_id)
            continue
        # Accept a bare list[str] (legacy resolvers) or a ResolvedModels.
        if isinstance(resolved, ResolvedModels):
            models = resolved.models
            source_anchor = resolved.source_anchor
            source_orientation = resolved.source_orientation
        else:
            models = resolved
            source_anchor = None
            source_orientation = None
        if not models:
            report.no_model_in_library.append(block.lib_id)
            continue
        # Orientation-convention rotation: a target footprint whose pad field
        # is rotated relative to its library source (rotation baked into pad
        # coordinates rather than the placement angle) needs the delta baked
        # into the model's (rotate ...).  theta is measured in the footprint
        # 2D frame; the model frame negates Y, so _apply_rotate_delta bakes a
        # model-frame Z of -theta.  When either orientation is underivable
        # (single-pad parts, LCSC-synthesized bodies) theta stays 0.
        target_orientation = _pad_field_orientation(body)
        theta = 0.0
        if source_orientation is not None and target_orientation is not None:
            theta = _normalize_angle(target_orientation - source_orientation)
        # Origin-convention offset, composed in the rotated frame: shift the
        # body by target_anchor - R_theta * source_anchor (pad centroids) so
        # the model lands on the target footprint's pads after rotation.  With
        # theta == 0 this reduces to the plain target - source centroid delta.
        target_anchor = _pad_anchor(body)
        dx = dy = 0.0
        if source_anchor is not None and target_anchor is not None:
            rsx, rsy = _rotate_point(source_anchor, theta)
            dx = target_anchor[0] - rsx
            dy = target_anchor[1] - rsy
        # Insert before the line that holds the footprint's closing paren.
        close_line_start = pcb_text.rfind("\n", 0, block.end) + 1
        child_indent = block.indent + "\t"
        text = "".join(
            _indent_block(_apply_rotate_delta(_apply_offset_delta(m, dx, dy), theta), child_indent)
            + "\n"
            for m in models
        )
        insertions.append((close_line_start, text))
        report.patched.append(block.lib_id)

    if not insertions:
        return pcb_text, report

    out: list[str] = []
    prev = 0
    for idx, text in insertions:
        out.append(pcb_text[prev:idx])
        out.append(text)
        prev = idx
    out.append(pcb_text[prev:])
    return "".join(out), report


def add_model_refs(
    pcb_path: Path | str,
    *,
    output_path: Path | str | None = None,
    library_paths: LibraryPaths | None = None,
    resolver: Resolver | None = None,
    allow_variants: bool = True,
    allow_substitutions: bool = True,
    lcsc_mapping: dict[str, str] | None = None,
    lcsc_fetch: bool = False,
    lcsc_cache_dir: Path | None = None,
    lcsc_warn: Callable[[str], None] | None = None,
    dry_run: bool = False,
) -> ModelPatchReport:
    """Patch missing 3D model refs into a ``.kicad_pcb`` file.

    Args:
        pcb_path: PCB file to patch.
        output_path: Where to write the patched file (default: in place).
        library_paths: Optional explicit KiCad library location (defaults to
            auto-detection of the installed KiCad footprint libraries).
        resolver: Optional custom lib-id resolver (overrides library_paths).
        allow_variants: Accept same-library footprint-name variants when the
            exact name is missing (visual fallback; reported in
            ``variant_matches``). Ignored when *resolver* is supplied.
        allow_substitutions: Accept curated cross-library substitutions when
            exact + variant matching both miss (reported in
            ``substitution_matches``). Ignored when *resolver* is supplied.
        lcsc_mapping: Optional ``lib_id -> C-number`` sidecar enabling the LCSC
            fetch tier (reported in ``lcsc_matches``). Ignored when *resolver*
            is supplied.
        lcsc_fetch: When True, fetch a missing LCSC STEP on a cache miss;
            default cache-only (no network). Ignored when *resolver* is
            supplied.
        lcsc_cache_dir: Optional LCSC STEP cache directory. Ignored when
            *resolver* is supplied.
        lcsc_warn: Optional ``callable(str)`` invoked on an LCSC fetch failure.
            Ignored when *resolver* is supplied.
        dry_run: When True, nothing is written.

    Returns:
        ModelPatchReport describing what was (or would be) inserted.
    """
    pcb_path = Path(pcb_path)
    text = pcb_path.read_text()
    variant_log: dict[str, str] = {}
    substitution_log: dict[str, str] = {}
    lcsc_log: dict[str, str] = {}
    if resolver is None:
        resolver = make_library_resolver(
            library_paths,
            allow_variants=allow_variants,
            allow_substitutions=allow_substitutions,
            variant_log=variant_log,
            substitution_log=substitution_log,
            lcsc_mapping=lcsc_mapping,
            lcsc_fetch=lcsc_fetch,
            lcsc_cache_dir=lcsc_cache_dir,
            lcsc_log=lcsc_log,
            lcsc_warn=lcsc_warn,
        )
    new_text, report = add_model_refs_to_text(text, resolver)
    # Only surface variants/substitutions that actually got patched in.
    report.variant_matches = {
        lib_id: stem for lib_id, stem in variant_log.items() if lib_id in report.patched
    }
    report.substitution_matches = {
        lib_id: sub for lib_id, sub in substitution_log.items() if lib_id in report.patched
    }
    report.lcsc_matches = {
        lib_id: cnum for lib_id, cnum in lcsc_log.items() if lib_id in report.patched
    }
    if report.changed and not dry_run:
        dest = Path(output_path) if output_path is not None else pcb_path
        dest.write_text(new_text)
    return report
