@dataclass
class ResolvedModels:
    """A resolver hit: the model block texts plus the source pad-field geometry.

    *models* are the dedented ``(model ...)`` node texts from the resolved
    library ``.kicad_mod``.  *source_anchor* is that footprint's own pad
    centroid (``None`` when it has no positioned pads), used to compute the
    origin-convention offset ``target_anchor - source_anchor`` at insertion
    time.  *source_orientation* is that footprint's pad-field orientation in
    degrees (``None`` when fewer than two positioned pads exist), used to
    compute the rotation ``theta = target_orientation - source_orientation``
    that a pad-field-rotated target footprint needs baked into its model.
    """

    models: list[str]
    source_anchor: tuple[float, float] | None = None
    source_orientation: float | None = None


# A resolver maps a lib id to its resolved models.  For backward
# compatibility it may return a bare ``list[str]`` (models only, no pad-1
# information -> zero offset applied) instead of a ``ResolvedModels``.
Resolver = Callable[[str], "ResolvedModels | list[str] | None"]


def _find_variant_mod(paths: LibraryPaths, library: str, name: str) -> Path | None:
    """Find a same-library name variant of a footprint (visual fallback).

    KiCad library footprints often exist only as suffixed variants of a
    family name (e.g. ``QFN-24-1EP_4x4mm_P0.5mm`` ships as
    ``QFN-24-1EP_4x4mm_P0.5mm_EP2.6x2.6mm``).  For 3D *models* — pure
    visual metadata — the variant's body is the right shape, so when the
    exact name is missing we accept a footprint in the same library whose
    name extends the requested one at a ``_`` or ``-`` boundary.

    Deterministic choice: plain variants beat ``ThermalVias`` specials,
    then shortest name, then lexicographic.
    """
    lib_dir = paths.get_library_path(library)
    if lib_dir is None:
        return None
    prefixes = (name + "_", name + "-")
    candidates = [p for p in lib_dir.glob("*.kicad_mod") if p.stem.startswith(prefixes)]
    if not candidates:
        return None
    candidates.sort(key=lambda p: ("ThermalVias" in p.stem, len(p.stem), p.stem))
    return candidates[0]


def make_library_resolver(
    library_paths: LibraryPaths | None = None,
    *,
    allow_variants: bool = True,
    allow_substitutions: bool = True,
    variant_log: dict[str, str] | None = None,
    substitution_log: dict[str, str] | None = None,
    lcsc_mapping: dict[str, str] | None = None,
    lcsc_fetch: bool = False,
    lcsc_cache_dir: Path | None = None,
    lcsc_log: dict[str, str] | None = None,
    lcsc_warn: Callable[[str], None] | None = None,
) -> Resolver:
    """Build a resolver that reads model refs from installed KiCad libraries.

    The resolver maps a footprint lib id (``"LED_THT:LED_D5.0mm"``) to the
    dedented ``(model ...)`` block(s) found in the corresponding installed
    ``.kicad_mod``.  Returns ``None`` when the library footprint can't be
    found and ``[]`` when it exists but carries no model reference.
    Results are cached per lib id.

    Resolution tiers (first hit wins):

    1. **Exact** installed footprint for ``library:name``.
    2. **Same-library variant** (``_find_variant_mod``) — a suffixed name in
       the same library, when ``allow_variants`` is set.
    3. **Cross-library substitution** (``model_substitutions``) — an explicit,
       curated ``lib_id -> lib_id`` mapping for generic/synthetic lib ids that
       have a body-compatible equivalent in a *different* library, when
       ``allow_substitutions`` is set.
    4. **LCSC/EasyEDA fetch-on-demand** (``lcsc_models``) — an explicit,
       committed ``lib_id -> C-number`` sidecar mapping whose STEP body is
       resolved from a local cache (fetched on demand from EasyEDA when
       ``lcsc_fetch`` is enabled), when *lcsc_mapping* is supplied.  The
       synthesized ``(model ...)`` ref points at ``${KCT_LCSC_3D_DIR}`` and is
       authored as **origin-centered** (``source_anchor=(0.0, 0.0)``) so the
       shared offset machinery lands the body on the target pad centroid.

    Args:
        library_paths: Explicit library location (default: auto-detect).
        allow_variants: When the exact footprint name is missing, fall back
            to a same-library name variant (see ``_find_variant_mod``).
        allow_substitutions: When exact + variant matching both miss, fall
            back to the curated cross-library substitution table.
        variant_log: Optional dict populated with ``lib_id -> variant stem``
            for every same-library variant match, so callers can report them.
        substitution_log: Optional dict populated with
            ``lib_id -> substitute lib_id`` for every cross-library
            substitution, so callers can report them.
        lcsc_mapping: Optional ``lib_id -> C-number`` sidecar mapping enabling
            the LCSC tier (tier 4).  Only lib ids present here are eligible.
        lcsc_fetch: When True, fetch a missing STEP from EasyEDA on a cache
            miss; default cache-only (no network).
        lcsc_cache_dir: Optional LCSC STEP cache directory (default:
            :func:`kicad_tools.pcb.lcsc_models.lcsc_cache_dir`).
        lcsc_log: Optional dict populated with ``lib_id -> C-number`` for every
            resolved LCSC match, so callers can report them.
        lcsc_warn: Optional ``callable(str)`` invoked on a fetch failure.
    """
    paths = library_paths if library_paths is not None else detect_kicad_library_path()
    cache: dict[str, ResolvedModels | None] = {}

    def _lookup_mod(lib_id: str) -> tuple[Path | None, str | None]:
        """Return (mod path, substitute lib id) for *lib_id*, or (None, None).

        The second element is set only when the match came from the
        cross-library substitution table.
        """
        library, name = parse_library_id(lib_id)
        if not library:
            return None, None
        mod_path = paths.get_footprint_file(library, name, fallback_search=False)
        if mod_path is not None:
            return mod_path, None
        if allow_variants:
            mod_path = _find_variant_mod(paths, library, name)
            if mod_path is not None:
                if variant_log is not None:
                    variant_log[lib_id] = mod_path.stem
                return mod_path, None
        if allow_substitutions:
            sub_id = substitute_lib_id(lib_id)
            if sub_id is not None:
                sub_lib, sub_name = parse_library_id(sub_id)
                if sub_lib:
                    mod_path = paths.get_footprint_file(sub_lib, sub_name, fallback_search=False)
                    if mod_path is not None:
                        return mod_path, sub_id
        return None, None

    def _resolve_lcsc(lib_id: str) -> ResolvedModels | None:
        """Tier 4: LCSC/EasyEDA fetch-on-demand.  Returns None when the lib id
        has no mapping, or the STEP is neither cached nor fetchable."""
        if not lcsc_mapping:
            return None
        lcsc_id = lcsc_mapping.get(lib_id)
        if not lcsc_id:
            return None
        # Imported lazily so the module (and the three installed-library tiers)
        # never depend on the LCSC client unless the LCSC tier is actually used.
        from kicad_tools.pcb.lcsc_models import resolve_lcsc_step, synthesize_model_block

        step_path = resolve_lcsc_step(
            lcsc_id,
            cache_dir=lcsc_cache_dir,
            fetch=lcsc_fetch,
            warn=lcsc_warn,
        )
        if step_path is None:
            # Skip-on-miss: report unresolved rather than emitting a ref to a
            # file that is known-absent at patch time.
            return None
        if lcsc_log is not None:
            lcsc_log[lib_id] = lcsc_id
        # Origin-authored body: explicit (0, 0) source anchor so the shared
        # offset math shifts by the full target pad centroid.
        return ResolvedModels(models=[synthesize_model_block(lcsc_id)], source_anchor=(0.0, 0.0))

    def resolve(lib_id: str) -> ResolvedModels | None:
        if lib_id in cache:
            return cache[lib_id]
        result: ResolvedModels | None = None
        if paths.found:
            mod_path, sub_id = _lookup_mod(lib_id)
            if mod_path is not None:
                try:
                    mod_text = mod_path.read_text()
                except OSError:
                    result = None
                else:
                    result = ResolvedModels(
                        models=extract_model_blocks(mod_text),
                        source_anchor=_pad_anchor(mod_text),
                        source_orientation=_pad_field_orientation(mod_text),
                    )
                    if sub_id is not None and substitution_log is not None:
                        substitution_log[lib_id] = sub_id
        if result is None:
            result = _resolve_lcsc(lib_id)
        cache[lib_id] = result
        return result

    return resolve


