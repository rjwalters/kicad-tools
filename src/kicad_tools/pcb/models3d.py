"""Patch ``(model ...)`` 3D model references into PCB footprints.

Boards generated by kicad-tools embed footprint s-expressions that carry
copper/silkscreen geometry but **no** ``(model ...)`` 3D model references,
so ``kicad-cli pcb render`` draws a bare board (pads only, no component
bodies).  Footprints sourced from the standard KiCad libraries have a
canonical model reference in their ``.kicad_mod`` source (e.g.
``${KICAD10_3DMODEL_DIR}/LED_THT.3dshapes/LED_D5.0mm.step``); this module
copies those references into an existing ``.kicad_pcb`` after the fact.

The patch is **pure text insertion**: for each footprint block whose lib id
resolves to an installed KiCad library footprint that has one or more
``(model ...)`` nodes, the nodes are inserted (re-indented) just before the
footprint's closing paren.  No other bytes of the file change -- copper,
placement, zones and nets are untouched, so DRC results are guaranteed
identical.

**Origin-convention offset.**  A STEP model is authored in its *library*
footprint's local frame (pad 1 at the library footprint's pad-1 position).
kicad-tools boards often reuse a canonical footprint *name* while placing
pads on a different origin convention -- e.g. a 2-pad 0.1" header whose pads
sit at ``(0, -1.27)`` / ``(0, +1.27)`` (centered on the origin) instead of
KiCad's ``(0, 0)`` / ``(0, 2.54)`` (pad 1 at origin).  Copying the model with
a zero offset then leaves the body shifted by the pad-1 delta (half the pitch
for a 2-pad part).  So before inserting a ``(model ...)`` node we add the
``target_pad1 - source_pad1`` delta into its ``(offset (xyz ...))`` node,
expressed in the KiCad 3D-model frame (X follows the footprint X, **Y is
negated** relative to the footprint 2D frame, Z is unchanged).  This keeps
the change pure render metadata -- only the newly inserted model block's own
offset numbers differ from the library source; no pre-existing byte moves and
no copper/pad/zone/net geometry changes.  Footprints that already share the
library's origin convention (most SMD parts, e.g. ``R_0805_2012Metric``)
compute a zero delta and are inserted verbatim.

**Orientation-convention rotation.**  Some generators bake a footprint's
rotation into the *pad coordinates* rather than expressing it as the
placement angle ``(at x y angle)`` -- e.g. board-07's 0.1" header lays its
pads along X (``(-10.16, 0)`` … ``(10.16, 0)``) while the canonical library
footprint (and its ``.step`` body) lays them along Y.  The placement angle is
``0`` and the copied model rotate is ``0``, so ``kicad-cli pcb render`` draws
the body 90° off from the copper.  This is the rotation analog of the origin
offset above: we derive the pad-field orientation of both the target block
and the source library footprint from two anchor pads (the pad-1→pad-2
vector), and bake ``theta = target - source`` into the inserted model's
``(rotate (xyz 0 0 ...))``.  Because the model frame's Y is negated versus the
footprint 2D frame, a footprint-2D rotation of ``theta`` maps to a model-frame
Z rotation of ``-theta``.  The centroid offset is composed **in the rotated
frame** (``offset_2d = target_centroid - R_theta * source_centroid``) so the
body still lands on the target pads.  Co-oriented footprints (all SMD parts,
headers already authored on the library convention, the origin-offset case
above) compute ``theta ~= 0`` and keep ``(rotate (xyz 0 0 0))`` plus the
existing offset verbatim.

Used by ``kct pcb add-3d-models``.
"""

