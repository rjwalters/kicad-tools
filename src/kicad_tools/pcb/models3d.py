# --------------------------------------------------------------------------
# Low-level s-expression text scanning (string-aware paren matching)
# --------------------------------------------------------------------------


def _skip_string(text: str, i: int) -> int:
    """Return the index just past a double-quoted string starting at *i*.

    Handles backslash escapes (KiCad escapes embedded quotes as ``\\"``).
    """
    assert text[i] == '"'
    i += 1
    n = len(text)
    while i < n:
        c = text[i]
        if c == "\\":
            i += 2
            continue
        if c == '"':
            return i + 1
        i += 1
    return n  # unterminated string: treat rest of text as the string


def _find_matching_paren(text: str, open_idx: int) -> int:
    """Index of the ``)`` closing the ``(`` at *open_idx* (string-aware)."""
    assert text[open_idx] == "("
    depth = 0
    i = open_idx
    n = len(text)
    while i < n:
        c = text[i]
        if c == '"':
            i = _skip_string(text, i)
            continue
        if c == "(":
            depth += 1
        elif c == ")":
            depth -= 1
            if depth == 0:
                return i
        i += 1
    raise ValueError("Unbalanced parentheses in s-expression text")


def _contains_token(text: str, token: str) -> bool:
    """True if ``(<token>`` appears outside of quoted strings in *text*."""
    needle = "(" + token
    i = 0
    n = len(text)
    while i < n:
        c = text[i]
        if c == '"':
            i = _skip_string(text, i)
            continue
        if c == "(" and text.startswith(needle, i):
            after = i + len(needle)
            if after >= n or text[after] in ' \t\n"()':
                return True
        i += 1
    return False


