# Model block extraction from .kicad_mod sources
# --------------------------------------------------------------------------


def extract_model_blocks(mod_text: str) -> list[str]:
    """Extract every ``(model ...)`` node from ``.kicad_mod`` text.

    Returns the raw node texts *dedented* so the first line starts with
    ``(model`` and continuation lines keep only their indentation relative
    to the node (tabs).
    """
    blocks: list[str] = []
    i = 0
    n = len(mod_text)
    while i < n:
        c = mod_text[i]
        if c == '"':
            i = _skip_string(mod_text, i)
            continue
        if c == "(" and mod_text.startswith("(model", i):
            after = i + len("(model")
            if after < n and mod_text[after] in ' \t\n"':
                end = _find_matching_paren(mod_text, i)
                raw = mod_text[i : end + 1]
                line_start = mod_text.rfind("\n", 0, i) + 1
                base_indent = mod_text[line_start:i]
                blocks.append(_dedent_block(raw, base_indent))
                i = end + 1
                continue
        i += 1
    return blocks


def _dedent_block(raw: str, base_indent: str) -> str:
    """Strip *base_indent* from the continuation lines of a raw node text."""
    if not base_indent:
        return raw
    lines = raw.split("\n")
    out = [lines[0]]
    for line in lines[1:]:
        out.append(line.removeprefix(base_indent))
    return "\n".join(out)


def _indent_block(block: str, indent: str) -> str:
    """Prefix every line of a dedented node text with *indent*."""
    return "\n".join(indent + line if line else line for line in block.split("\n"))


