@dataclass
class FootprintBlock:
    """A ``(footprint "Lib:Name" ...)`` block located in raw PCB text."""

    lib_id: str
    start: int
    """Index of the opening ``(``."""
    end: int
    """Index of the closing ``)`` (inclusive)."""
    indent: str
    """Leading whitespace of the line containing the opening paren."""


def iter_footprint_blocks(pcb_text: str) -> list[FootprintBlock]:
    """Locate every top-level footprint block in *pcb_text*.

    Blocks are found with a string-aware scanner, so ``(footprint`` inside a
    quoted string is never mis-detected.
    """
    blocks: list[FootprintBlock] = []
    i = 0
    n = len(pcb_text)
    while i < n:
        c = pcb_text[i]
        if c == '"':
            i = _skip_string(pcb_text, i)
            continue
        if c == "(" and pcb_text.startswith("(footprint", i):
            after = i + len("(footprint")
            if after < n and pcb_text[after] in ' \t\n"':
                end = _find_matching_paren(pcb_text, i)
                # lib id is the first quoted string after the token
                q = pcb_text.find('"', after, end)
                lib_id = ""
                if q != -1:
                    q_end = _skip_string(pcb_text, q)
                    lib_id = pcb_text[q + 1 : q_end - 1]
                line_start = pcb_text.rfind("\n", 0, i) + 1
                indent = pcb_text[line_start:i]
                if indent.strip() == "":
                    blocks.append(FootprintBlock(lib_id=lib_id, start=i, end=end, indent=indent))
                i = end + 1
                continue
        i += 1
    return blocks


