/**
 * Prebuild step: stage board render PNGs and manufacturing downloads into
 * `site/public/` so the static Astro build can serve them.
 *
 * Why this exists
 * ---------------
 * Astro is configured with `output: "static"` and the board artifacts live
 * **outside** `site/` (under the repo's `boards/<id>/output/`). Astro cannot
 * import or bundle assets from outside its project root, so we copy the files
 * into `site/public/boards/<slug>/...` at build time. Anything under `public/`
 * is served verbatim at the site root, so a copied file at
 * `site/public/boards/<slug>/renders/pcb-front.png` is reachable at the URL
 * `/boards/<slug>/renders/pcb-front.png` — which is exactly what
 * `BoardCard.astro` (gallery thumbnails) and `[slug].astro` (detail gallery +
 * downloads) reference.
 *
 * What gets staged
 * ----------------
 *   - Renders:        `boards/<id>/output/renders/*.png`
 *                  →  `site/public/boards/<slug>/renders/<file>`
 *                  →  served at `/boards/<slug>/renders/<file>`
 *   - Manufacturing:  the downloadable files under
 *                     `boards/<id>/output/manufacturing/` that the detail page
 *                     links (the kicad_project.zip plus optional report.pdf /
 *                     BOM / CPL). Only an allow-list of known download files is
 *                     copied — gerbers/, images/, and other intermediate
 *                     directories are intentionally NOT staged.
 *                  →  `site/public/boards/<slug>/manufacturing/<file>`
 *                  →  served at `/boards/<slug>/manufacturing/<file>`
 *   - PCB file:       the board's routed `.kicad_pcb` (preferred) or a
 *                     non-routed `.kicad_pcb` fallback from `output/`, staged
 *                     under a fixed name so the detail page's interactive
 *                     KiCanvas viewer can fetch it.
 *                  →  `site/public/boards/<slug>/board.kicad_pcb`
 *                  →  served at `/boards/<slug>/board.kicad_pcb`
 *
 * Discovery rules mirror `src/data/loadBoards.ts` (and the Python producer):
 * immediate subdirectories of `boards/`, skipping hidden / `_`-prefixed
 * entries, and descending one level into `boards/external/`. The board's
 * directory name is its `slug` — the same key the loader emits — so the staged
 * path lines up with the `Board.slug` used by the page components.
 *
 * Resilience: this script NEVER fails the build. A fresh checkout has zero
 * renders / manufacturing packages (they are generated by `kct render` and
 * `kct manufacture`), so a missing `boards/` tree or missing `renders/` /
 * `manufacturing/` directory is a normal no-op, not an error. The staged output
 * directory (`site/public/boards/`) is git-ignored and regenerated on every
 * build.
 */

import {
  readdirSync,
  existsSync,
  statSync,
  mkdirSync,
  copyFileSync,
  rmSync,
} from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const SITE_DIR = resolve(SCRIPT_DIR, "..");

/**
 * Allow-list of manufacturing download files staged for the detail page.
 *
 * Only these named files are copied (not the whole `manufacturing/` tree) so we
 * never publish multi-megabyte gerber/image intermediates. `kicad_project.zip`
 * is the primary package (also referenced by `board.json`'s
 * `manufacturing_package`); the rest are optional supplementary downloads that
 * the detail page links only when present.
 */
const MANUFACTURING_FILES = [
  "kicad_project.zip",
  "report.pdf",
  "bom_jlcpcb.csv",
  "cpl_jlcpcb.csv",
];

/** True if `path` exists and is a directory. */
function isDir(path) {
  try {
    return statSync(path).isDirectory();
  } catch {
    return false;
  }
}

/** True if `path` exists and is a regular file. */
function isFile(path) {
  try {
    return statSync(path).isFile();
  } catch {
    return false;
  }
}

/**
 * Resolve the repository's `boards/` directory.
 *   1. `KCT_BOARDS_DIR` env override (tests / CI).
 *   2. `<site>/../boards` — the repo root is one level above `site/`.
 */
function boardsDir() {
  const override = process.env.KCT_BOARDS_DIR;
  if (override) return resolve(override);
  return resolve(SITE_DIR, "..", "boards");
}

/**
 * Enumerate `{ slug, dir }` for every board directory under `root`, mirroring
 * the loader's discovery so staged renders line up with `Board.slug`.
 */
function discoverBoards(root) {
  if (!isDir(root)) return [];

  const boards = [];
  for (const entry of readdirSync(root).sort()) {
    if (entry.startsWith(".") || entry.startsWith("_")) continue;
    const full = join(root, entry);
    if (!isDir(full)) continue;

    if (entry === "external") {
      // Group directory: descend one level. The slug is the sub-entry name.
      for (const sub of readdirSync(full).sort()) {
        if (sub.startsWith(".")) continue;
        const subFull = join(full, sub);
        if (isDir(subFull)) boards.push({ slug: sub, dir: subFull });
      }
      continue;
    }
    boards.push({ slug: entry, dir: full });
  }
  return boards;
}

function main() {
  const root = boardsDir();
  const destRoot = join(SITE_DIR, "public", "boards");

  // Start from a clean staging tree so removed renders don't linger.
  if (existsSync(destRoot)) {
    rmSync(destRoot, { recursive: true, force: true });
  }

  if (!isDir(root)) {
    console.log(
      `[copy-renders] boards directory not found at ${root}; nothing to stage`,
    );
    return;
  }

  let copied = 0;
  let boardsWithRenders = 0;
  let mfgCopied = 0;
  let boardsWithMfg = 0;
  let pcbCopied = 0;

  for (const { slug, dir } of discoverBoards(root)) {
    // --- Renders ---
    const srcRenders = join(dir, "output", "renders");
    if (isDir(srcRenders)) {
      const pngs = readdirSync(srcRenders).filter((f) =>
        f.toLowerCase().endsWith(".png"),
      );
      if (pngs.length > 0) {
        const destRenders = join(destRoot, slug, "renders");
        mkdirSync(destRenders, { recursive: true });
        for (const file of pngs) {
          copyFileSync(join(srcRenders, file), join(destRenders, file));
          copied += 1;
        }
        boardsWithRenders += 1;
      }
    }

    // --- Manufacturing downloads (allow-listed files only) ---
    const srcMfg = join(dir, "output", "manufacturing");
    if (isDir(srcMfg)) {
      const present = MANUFACTURING_FILES.filter((f) => isFile(join(srcMfg, f)));
      if (present.length > 0) {
        const destMfg = join(destRoot, slug, "manufacturing");
        mkdirSync(destMfg, { recursive: true });
        for (const file of present) {
          copyFileSync(join(srcMfg, file), join(destMfg, file));
          mfgCopied += 1;
        }
        boardsWithMfg += 1;
      }
    }

    // --- PCB file (for the interactive viewer) ---
    // Stage the board's `.kicad_pcb` under a fixed name so the detail page can
    // point KiCanvas at `/boards/<slug>/board.kicad_pcb`. Prefer a routed
    // variant; fall back to any other `.kicad_pcb` in `output/`. Boards with no
    // `output/` directory (e.g. `00-simple-led`) are skipped silently — the
    // detail page renders a "no interactive view" fallback for those.
    const srcOutput = join(dir, "output");
    if (isDir(srcOutput)) {
      const pcbs = readdirSync(srcOutput)
        .filter((f) => f.endsWith(".kicad_pcb"))
        .sort();
      const chosen = pcbs.find((f) => f.includes("_routed")) ?? pcbs[0];
      if (chosen) {
        const destBoard = join(destRoot, slug);
        mkdirSync(destBoard, { recursive: true });
        copyFileSync(join(srcOutput, chosen), join(destBoard, "board.kicad_pcb"));
        pcbCopied += 1;
      }
    }
  }

  console.log(
    `[copy-renders] staged ${copied} render(s) from ${boardsWithRenders} board(s), ` +
      `${mfgCopied} manufacturing file(s) from ${boardsWithMfg} board(s), ` +
      `and ${pcbCopied} PCB file(s) ` +
      `into ${destRoot}`,
  );
}

main();
